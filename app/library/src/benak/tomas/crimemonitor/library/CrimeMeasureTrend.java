package benak.tomas.crimemonitor.library;

/**
 * An enumeration used to describe the trend for the committed crimes count
 * over the period of time.
 * 
 * @author Tomik
 */
public enum CrimeMeasureTrend
{
	/**
	 * the crime count tends to go down
	 */
	Down,
	/**
	 * the crime count is stable
	 */
	Stable,
	/**
	 * the crime count tends to go up
	 */
	Up
}
